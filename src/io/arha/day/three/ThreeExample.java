package io.arha.day.three;
 

public class ThreeExample {

	
	
	public static void main(String[] args) {
		
		String className = "JAVA CLASS 1";

		switch (className) {
		case "JAVA CLASS 1":
			//
			System.out.println("_________JAVA CLASS 1________");
			break;
		case "JAVA CLASS 2":
			System.out.println("________JAVA CLASS 2_________");
			break;
		case "JAVA CLASS 3":
			System.out.println("_______JAVA CLASS 3__________");
			break;
		default:
			System.out.println("________default_________");
		}
		System.out.println();

	}

}
